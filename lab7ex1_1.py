






# Открываем файл "Students.txt" в режиме записи ('w'), используя менеджер контекста with
# Если файла нет, он будет создан. Если файл есть — его содержимое будет перезаписано.
with open("Students.txt", "w") as file:
    # Записываем пять имён, каждое на новой строке
    file.write("Андрей\n")  # Первая строка — имя "Андрей"
    file.write("Мария\n")   # Вторая строка — имя "Мария"
    file.write("Игорь\n")   # Третья строка — имя "Игорь"
    file.write("Светлана\n")# Четвёртая строка — имя "Светлана"
    file.write("Дмитрий\n") # Пятая строка — имя "Дмитрий"

# Проверяем, что файл действительно создан и содержит данные
# Открываем файл в режиме чтения ('r') и выводим содержимое
with open("Students.txt", "r") as file:
    print("Содержимое файла после создания:")
    print(file.read())  # Выводим весь текст из файла
