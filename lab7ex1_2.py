










# Запрашиваем у пользователя ввод нового имени
new_name = input("Введите новое имя для добавления в файл: ")

# Открываем файл в режиме добавления ('a'), чтобы дописать в конец файла
with open("Students.txt", "a") as file:
    file.write(new_name + "\n")  # Добавляем новое имя и перенос строки

# Повторно открываем файл в режиме чтения ('r') и выводим всё содержимое
with open("Students.txt", "r") as file:
    print("Содержимое файла после добавления нового имени:")
    print(file.read())  # Печатаем обновлённое содержимое
