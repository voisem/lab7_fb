









# Запрашиваем у пользователя имя нового файла
filename = input("Введите имя нового файла (с расширением .txt): ")

# Запрашиваем текст, который нужно записать в файл
user_text = input("Введите текст для записи в файл: ")

# Преобразуем весь текст в верхний регистр (заглавные буквы)
upper_text = user_text.upper()

# Открываем файл в режиме записи и записываем преобразованный текст
with open(filename, "w") as file:
    file.write(upper_text)  # Записываем текст в заглавных буквах

# Проверяем результат
print(f"Файл '{filename}' успешно создан с текстом:")
print(upper_text)
